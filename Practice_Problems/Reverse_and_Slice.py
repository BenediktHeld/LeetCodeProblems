# Exercise 2: Reverse and Slice
#
# You have a list of student names:
students = ["Alice", "Bob", "Charlie", "Diana", "Eve", "Frank"]

# Task:
# 1. Reverse the list
students.reverse()
print(students)

# 2. Extract the middle two students from the REVERSED list using slicing
print(students[2:4])

# Expected output (example):
# Reversed: ['Frank', 'Eve', 'Diana', 'Charlie', 'Bob', 'Alice']
# Middle two: ['Diana', 'Charlie']

